CREATE TABLE IF NOT EXISTS public.adhkar (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  text text NOT NULL,
  is_active boolean NOT NULL DEFAULT true,
  sort_order integer NOT NULL DEFAULT 0,
  created_by uuid REFERENCES public.users(id),
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT adhkar_text_nonempty CHECK (length(trim("text")) > 0)
);

CREATE INDEX IF NOT EXISTS adhkar_is_active_idx ON public.adhkar(is_active);

ALTER TABLE public.adhkar ENABLE ROW LEVEL SECURITY;

CREATE POLICY "adhkar_select"
  ON public.adhkar FOR SELECT TO authenticated
  USING (true);

CREATE POLICY "adhkar_insert"
  ON public.adhkar FOR INSERT TO authenticated
  WITH CHECK (EXISTS (
    SELECT 1 FROM public.users
    WHERE id = (SELECT auth.uid()) AND role = 'admin' AND is_active = true
  ));

CREATE POLICY "adhkar_update"
  ON public.adhkar FOR UPDATE TO authenticated
  USING (EXISTS (
    SELECT 1 FROM public.users
    WHERE id = (SELECT auth.uid()) AND role = 'admin' AND is_active = true
  ))
  WITH CHECK (EXISTS (
    SELECT 1 FROM public.users
    WHERE id = (SELECT auth.uid()) AND role = 'admin' AND is_active = true
  ));

CREATE POLICY "adhkar_delete"
  ON public.adhkar FOR DELETE TO authenticated
  USING (EXISTS (
    SELECT 1 FROM public.users
    WHERE id = (SELECT auth.uid()) AND role = 'admin' AND is_active = true
  ));

